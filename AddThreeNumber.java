package corejava;
public class AddThreeNumber {
	public static int add(int a, int b, int c ,int d)
	{
		int r=a+b+c+d;
		return r;
		
		
	}

	public static void main(String[] args) {
		int r=add(1,20,20,45 );
		System.out.println("Addition of three number is "+r);
		System.out.println();

	}

}
